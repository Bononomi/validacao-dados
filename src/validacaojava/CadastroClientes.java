    private boolean validarCampos() {
        CPFValidator validator = new CPFValidator();

        try {
           
           
            String nome = cpNome.getText();
            String telefone = cpTelefone.getText();

            if (nome.trim().equals("")) {
                JOptionPane.showMessageDialog(null, "Informe o nome!");
                return false;       
            } 
             
            validator.assertValid(cpCPF.getText());
             
            
            telefone = telefone.replaceAll(" ","");
            telefone = telefone.replaceAll("-","");
                        
            if (telefone.trim().equals("") || telefone.trim().equals("()")){
                JOptionPane.showMessageDialog(null, "Informe o telefone!");
                return false;
            }
    
            
            if (comboSexo.getSelectedItem().equals("Selecione")) {
                JOptionPane.showMessageDialog(null, "Selecione o sexo!");
                return false;
            } else {
                return true;
            }
           
            
        } catch (InvalidStateException e) { // exception lanÃ§ada quando o documento Ã© invÃ¡lido
            JOptionPane.showMessageDialog(null, "CPF INVÃLIDO!");
            return false;
        }

    }

